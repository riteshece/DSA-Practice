// what is javascript and how it works?:
// JavaScript is a scripting language for the web. It is an interpreted language, 
//which means it does not need a compiler to translate its code like C or C++.
// JavaScript code runs directly in a web browser.
// JavaScript works with HTML and CSS to build web apps or web pages. 
//JavaScript is supported by most modern web browsers like Google Chrome, Firefox, Safari, Microsoft Edge, Opera, etc.
// Most mobile browsers for Android and iPhone now support JavaScript as well.

// JavaScript controls the dynamic elements of web pages. It works in web browsers and, 
//more recently, on web servers as well. Application Programming Interfaces (API) are also supported by JavaScript,
// giving you more functionality.
